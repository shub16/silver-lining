import sys
from PyQt4.QtGui import *

app = QApplication(sys.argv)
button = QPushButton("Button")
button.show()

window = QWidget()
textBox = QLineEdit()


layout = QVBoxLayout(window)
layout.addWidget(textBox)
layout.addWidget(button)


window.resize(200,140)
window.setWindowTitle("WindowTitle")
window.show()
sys.exit(app.exec_())
